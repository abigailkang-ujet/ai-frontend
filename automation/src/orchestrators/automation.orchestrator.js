/**
 * Process AI task: Figma → Code Generation → GitHub PR
 * @param {string} issueKey - Jira issue key
 * @param {string} summary - Issue summary
 * @param {string} description - Issue description
 * @param {string} figmaUrl - Figma URL
 */
async function processAITask(issueKey, summary, description, figmaUrl) {
  console.log(`\n🚀 Processing task for ${issueKey}...`);

  let figmaData = null;
  let automationResult = null;

  try {
    // 1. Figma 데이터 가져오기
    console.log('📥 Step 1: Fetching Figma data...');
    figmaData = await figmaService.getDesignFromUrl(figmaUrl);
    console.log('✅ Figma 데이터 가져오기 완료!');
    console.log(`📐 파일명: ${figmaData.file.name}`);
  } catch (error) {
    console.error('❌ Figma 처리 중 오류:', error.message);
    // 오류가 발생하면 알림 보내고 종료
    await notificationOrchestrator.notifyFailure(
      issueKey,
      summary,
      figmaUrl,
      null,
      `Figma 데이터를 가져올 수 없습니다: ${error.message}`
    );
    return;
  }

  try {
    // 2. AI 코드 생성 & GitHub PR 자동 생성
    console.log('🤖 Step 2: AI 코드 생성 & GitHub PR 생성...');
    automationResult = await automateCodeGeneration(issueKey, summary, figmaUrl, figmaData);

    if (automationResult.success) {
      console.log('✅ 자동화 성공!');

      // 3. 성공 알림
      await notificationOrchestrator.notifySuccess(
        issueKey,
        summary,
        figmaUrl,
        figmaData,
        automationResult
      );

    } else {
      throw new Error(automationResult.error || 'Unknown error');
    }

  } catch (error) {
    console.error('❌ 자동화 실패:', error.message);

    // 실패 알림
    await notificationOrchestrator.notifyFailure(
      issueKey,
      summary,
      figmaUrl,
      figmaData,
      error.message
    );
  }

  console.log('✨ Task completed!');
}

/**
 * Automate code generation: AI → GitHub PR
 * This is where the orchestration logic from githubService was moved
 */
async function automateCodeGeneration(issueKey, summary, figmaUrl, figmaData) {
  try {
    console.log('\n🚀 자동 코드 생성 시작...');

    // 1. AI로 코드 생성 (임시로 템플릿 사용)
    console.log('🤖 Step 1: 코드 생성...');

    // NOTE: This orchestrator is deprecated. Use issueAutomationOrchestrator instead.
    // The new flow generates AI task files instead of direct code generation.
    // See: src/orchestrators/issueAutomationOrchestrator.js

    // 임시 템플릿 코드
    const generatedCode = {
      fileName: 'DocumentationPage.tsx',
      componentCode: generateTemplateCode(figmaUrl),
      explanation: 'Documentation page component with navigation, sidebar, and main content area. Generated from Figma design.',
    };
    
    // 2. 브랜치 이름 생성
    const branchName = `feat/${issueKey}-${Date.now()}`;
    console.log(`📝 Step 2: 브랜치 생성 (${branchName})...`);
    
    // 3. 메인 브랜치 SHA 가져오기
    const mainSHA = await githubService.getMainBranchSHA();
    
    // 4. 새 브랜치 생성
    await githubService.createBranch(branchName, mainSHA);
    
    // 5. 생성된 코드 파일 커밋
    console.log('💾 Step 3: 코드 파일 커밋...');
    const fileName = generatedCode.fileName || 'Component.tsx';
    const filePath = `generated-components/${fileName}`;
    
    await githubService.createOrUpdateFile(
      branchName,
      filePath,
      generatedCode.componentCode,
      `feat(${issueKey}): Add ${fileName}\n\nGenerated from Figma design\n${figmaUrl}`
    );
    
    // 6. PR 생성
    console.log('🎉 Step 4: PR 생성...');
    const prTitle = `[${issueKey}] ${summary}`;
    const prBody = `## 🎨 Figma Design Implementation

### Jira Ticket
- **Key:** ${issueKey}
- **Summary:** ${summary}

### Figma Design
- **URL:** ${figmaUrl}

### Generated Components
- \`${filePath}\`

### AI Explanation
${generatedCode.explanation || 'AI generated component from Figma design'}

---
🤖 Auto-generated by AI
`;

    const pr = await githubService.createPullRequest(branchName, prTitle, prBody, false);

    console.log('✨ 자동화 완료!');

    return {
      success: true,
      pr: pr,
      branchName: branchName,
      files: [filePath],
    };

  } catch (error) {
    console.error('❌ 자동화 실패:', error.message);
    return {
      success: false,
      error: error.message,
    };
  }
}

